start:
    ; Set video mode 13h (320x200, 256 colors)
    mov ax, 0x0013
    int 0x10

    ; Set ES to point to VGA memory segment
    mov ax, 0xA000
    mov es, ax

    ; Point SI to the string to render
    mov si, message
    xor bx, bx        ; BX = (row << 8 | col), starting at (0,0)
    call draw_string

    ; Wait for keypress
    xor ah, ah
    int 0x16

    ; Return to caller
    retf
