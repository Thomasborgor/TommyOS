; ---------------------------------------
; Draw null-terminated string from SI
; BX = Y:BH, X:BL (char position)
; ---------------------------------------
draw_string:
    pusha
.next_char:
    lodsb
    or al, al
    jz .done

    cmp al, 32
    jb .skip
    cmp al, 126
    ja .skip

    push si
    push bx
    call draw_char
    pop bx
    pop si

.skip:
    inc bl
    cmp bl, 40         ; max columns (320 / 8)
    jb .next_char
	mov bl, 8
	add bh, 8
    jmp .next_char

.done:
    popa
    ret

; ---------------------------------------
; Draw character in AL at (BH: row, BL: col)
; ---------------------------------------
draw_char:
    pusha

    ; Get font offset: (AL - 32) * 8
    sub al, 32
    movzx si, al
    shl si, 3
    add si, font8x8

    ; Compute starting pixel offset
    movzx ax, bl
    shl ax, 3          ; X * 8
    movzx dx, bh
	shl dx, 2
	
    ; DI = (Y * 8) * 320 + X * 8
    mov di, dx
    shl dx, 6
    shl di, 2
    add di, dx
    add di, ax

    mov cx, 8
.row_loop:
    mov al, [si]
    inc si

    mov bx, di
    mov bp, 0          ; column index (left to right)

.next_bit:
    test al, 1
    jz .skip
    mov byte [es:bx], 0x0F
.skip:
    inc bx
    inc bp
    shr al, 1
    cmp bp, 8
    jl .next_bit

    add di, 320
    loop .row_loop

    popa
    ret

